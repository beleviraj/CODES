CREATE DATABASE STUDENT;
USE STUDENT;

CREATE TABLE STUDENT(
STUDENT_ID VARCHAR(20),
STUDENT_NAME VARCHAR(20),
STUDENT_EMAIL VARCHAR(20)
);

CREATE TABLE SUBJECT(
SUBJECT_ID VARCHAR(20),
SUBJECT_NAME VARCHAR(20)
);
ALTER TABLE STUDENT
MODIFY STUDENT_ID VARCHAR(20) PRIMARY KEY ;

ALTER TABLE SUBJECT
MODIFY SUBJECT_ID VARCHAR(20) PRIMARY KEY;

CREATE TABLE GRADES(
GRADE_ID VARCHAR(20) PRIMARY KEY,
STU_ID VARCHAR(20),
FOREIGN KEY (STU_ID) REFERENCES STUDENT(STUDENT_ID),
SUB_ID VARCHAR(20),
FOREIGN KEY (SUB_ID) REFERENCES SUBJECT(SUBJECT_ID),
GRADE VARCHAR(5)
);
DESCRIBE GRADES;

